    /**
     * Acción ejecutada cuando el usuario presiona el botón de crear tarea.
     * Captura la información ingresada en los campos y la procesa.
     * 
     * @param evt el evento de acción generado al presionar el botón.
     */
    private void btnCrearTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearTareaActionPerformed
        // Obtén los datos de los campos
    String usuario = (String) cmbUsuariosCrearTarea.getSelectedItem();
    String nombreTarea = txtNombreTarea.getText();
    String descripcion = txtDescripcionCrearTarea.getText();
    String prioridad = (String) boxPrioridadCrearTarea.getSelectedItem();
    String estado = (String) boxEstadoCrearTarea.getSelectedItem();
    String fechaInicio = txtFechaInicioCrearTarea.getText();
    String fechaFin = txtFechaFinCrearTarea.getText();

    // Verifica que todos los campos estén llenos
    if (usuario.isEmpty() || nombreTarea.isEmpty() || descripcion.isEmpty() || 
        fechaInicio.isEmpty() || fechaFin.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
        return;
    }

    // Ruta del archivo
    String ruta = "C:\\Users\\hp\\Documents\\2024\\SEGUNDO SEMESTRE\\METODOS Y TALLER DE PROGRAMACION\\Proyecto Netbeans\\Gestion de Tareas\\tareas.txt";

    try (BufferedWriter writer = new BufferedWriter(new FileWriter(ruta, true))) {
        // Escribe los datos en el archivo con formato nuevo
        writer.write(usuario + ";" + nombreTarea + ";" + descripcion + ";" + prioridad + ";" 
                     + estado + ";" + fechaInicio + ";" + fechaFin);
        writer.newLine();
        javax.swing.JOptionPane.showMessageDialog(this, "Tarea creada exitosamente.");

        // Limpia los campos después de crear la tarea
        cmbUsuariosCrearTarea.setSelectedIndex(0);
        txtNombreTarea.setText("");
        txtDescripcionCrearTarea.setText("");
        boxPrioridadCrearTarea.setSelectedIndex(0);
        boxEstadoCrearTarea.setSelectedIndex(0);
        txtFechaInicioCrearTarea.setText("");
        txtFechaFinCrearTarea.setText("");

        // Actualiza la tabla en panelListaTareas
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                panelListaTareas.cargarTareasEnTabla(); // Cargar tareas actualizadas
                panelListaTareas.revalidate();
                panelListaTareas.repaint();
            }
        });

        // Cerrar la ventana de CrearTarea
        this.dispose();

    } catch (IOException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar la tarea: " + e.getMessage());
    }
    }//GEN-LAST:event_btnCrearTareaActionPerformed

    
