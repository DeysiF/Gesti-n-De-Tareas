    /**
     * Carga los usuarios disponibles desde un archivo de texto en el JComboBox.
     * Los datos del archivo deben estar separados por ";".
     */
     private void cargarUsuariosEnComboBox() {
        String ruta = "C:\\Users\\hp\\Documents\\2024\\SEGUNDO SEMESTRE\\METODOS Y TALLER DE PROGRAMACION\\Proyecto Netbeans\\Gestion de Tareas\\usuarioscreados.txt"; // Ruta al archivo
        DefaultComboBoxModel<String> modelo = new DefaultComboBoxModel<>();

        try (BufferedReader reader = new BufferedReader(new FileReader(ruta))) {
            String linea;

            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(";"); // Separar por ";"
                if (datos.length >= 3) { // Verifica que haya suficientes datos
                    modelo.addElement(datos[2]); // Agregar el nombre de usuario (posición 2)
                }
            }
        } catch (IOException e) {
            System.err.println("Error al cargar los usuarios: " + e.getMessage());
        }

        cmbUsuariosCrearTarea.setModel(modelo); // Asignar el modelo al JComboBox
    }
    


