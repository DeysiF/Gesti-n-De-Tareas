    /**
     * Método para manejar el evento de edición del usuario.
     * Valida los campos, actualiza el archivo de usuarios y refresca la lista.
     */
    private void btnEditarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarUsuarioActionPerformed
   // Obtenemos los datos de los campos
    String nombre = txtNombreCrearUsuario.getText();
    String correo = txtCorreoCrearUsuario.getText();
    String usuario = txtUsuarioCrearUsuario.getText();
    String contrasena = new String(jContraseñaCrearUsuario.getPassword());

    // Verifica que todos los campos estén completos
    if (nombre.isEmpty() || correo.isEmpty() || usuario.isEmpty() || contrasena.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
        return;
    }

    // Ruta al archivo de usuarios existentes
    String ruta = "C:\\Users\\hp\\Documents\\2024\\SEGUNDO SEMESTRE\\METODOS Y TALLER DE PROGRAMACION\\Proyecto Netbeans\\Gestion de Tareas\\usuarioscreados.txt";
    File archivo = new File(ruta);

    // Crear un StringBuilder para almacenar el nuevo contenido del archivo
    StringBuilder nuevoContenido = new StringBuilder();

    try (BufferedReader reader = new BufferedReader(new FileReader(archivo))) {
        String linea;
        boolean usuarioEditado = false;

        while ((linea = reader.readLine()) != null) {
            String[] datos = linea.split(";");
            
            // Aquí se usa el nombre de usuario original (que se cargó al abrir la ventana)
            if (datos.length >= 4 && datos[2].equals(usuarioSeleccionado)) {  // Compara el nombre de usuario original
                // Si encuentra el usuario, reemplaza la línea con los nuevos datos
                nuevoContenido.append(nombre).append(";")
                        .append(correo).append(";")
                        .append(usuario).append(";")
                        .append(contrasena).append("\n");
                usuarioEditado = true;
            } else {
                nuevoContenido.append(linea).append("\n");  // Escribe las líneas sin cambios
            }
        }

        // Verifica si se encontró el usuario para editar
        if (!usuarioEditado) {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado.");
            return;
        }

        // Sobrescribir el archivo original con el nuevo contenido
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivo))) {
            writer.write(nuevoContenido.toString());
        }

        // Actualiza la tabla en jPanelListaUsuarios
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                panelListaUsuarios.cargarUsuariosEnTabla(); // Llama al método que actualiza la tabla
                panelListaUsuarios.revalidate();
                panelListaUsuarios.repaint();
            }
        });

        // Cierra la ventana de edición
        this.dispose();
        JOptionPane.showMessageDialog(this, "Usuario editado con éxito.");

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al editar el usuario: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEditarUsuarioActionPerformed

