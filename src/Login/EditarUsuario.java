     /**
     * Carga los datos del usuario seleccionado desde un archivo de texto y
     * los muestra en los campos correspondientes.
     */
     private void cargarDatosUsuario() {
        String ruta = "C:\\Users\\hp\\Documents\\2024\\SEGUNDO SEMESTRE\\METODOS Y TALLER DE PROGRAMACION\\Proyecto Netbeans\\Gestion de Tareas\\usuarioscreados.txt";
        try (BufferedReader reader = new BufferedReader(new FileReader(ruta))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(";");

                // Verificar que sea el usuario seleccionado
                if (datos[1].equals(correoSeleccionado) && datos[2].equals(usuarioSeleccionado)) {
                    // Llenar los campos con los datos correspondientes
                    txtNombreCrearUsuario.setText(datos[0]);  // Nombre y Apellido
                    txtCorreoCrearUsuario.setText(datos[1]);  // Correo
                    txtUsuarioCrearUsuario.setText(datos[2]); // Usuario
                    jContraseñaCrearUsuario.setText(datos[3]); // Contraseña
                    break;  // No necesitamos seguir buscando, ya encontramos al usuario
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los datos del usuario: " + e.getMessage());
        }
    }
    

