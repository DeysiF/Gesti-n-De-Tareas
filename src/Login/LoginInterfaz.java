    /**
     * Maneja el evento de clic en el botón "Iniciar Sesión".
     * Verifica las credenciales ingresadas y redirige al usuario o administrador según corresponda.
     * 
     * @param evt el evento de acción asociado al botón.
     */
    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        
    String gmail = txtGmail.getText().trim();
    String password = new String(jContraseña.getPassword()).trim();

    // Validar que ambos campos estén llenos
    if (gmail.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe ingresar su Gmail y contraseña", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    boolean loginExitoso = false;
    boolean esAdmin = false;
    String nombreUsuario = "";
    
    // Verificar si es el administrador
    try (BufferedReader adminReader = new BufferedReader(new FileReader("C:\\Users\\hp\\Documents\\2024\\SEGUNDO SEMESTRE\\METODOS Y TALLER DE PROGRAMACION\\Proyecto Netbeans\\Gestion de Tareas\\admin.txt"))) {
        String lineaAdmin = adminReader.readLine();
        if (lineaAdmin != null) {
            String[] datosAdmin = lineaAdmin.split(",");
            if (datosAdmin[0].equals(gmail) && datosAdmin[1].equals(password)) {
                loginExitoso = true;
                esAdmin = true;
                nombreUsuario = "Administrador";
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al leer el archivo de administrador", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Si no es admin, verificar en usuarios normales
    if (!esAdmin) {
        try (BufferedReader reader = new BufferedReader(new FileReader("C:\\Users\\hp\\Documents\\2024\\SEGUNDO SEMESTRE\\METODOS Y TALLER DE PROGRAMACION\\Proyecto Netbeans\\Gestion de Tareas\\usuarios.txt"))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(",");
                if (datos.length == 3) { 
                    String archivoGmail = datos[0];
                    String archivoPassword = datos[1];
                    String archivoNombreUsuario = datos[2];

                    if (archivoGmail.equals(gmail) && archivoPassword.equals(password)) {
                        loginExitoso = true;
                        nombreUsuario = archivoNombreUsuario;
                        break;
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo de usuarios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    if (loginExitoso) {
        if (esAdmin) {
            JOptionPane.showMessageDialog(this, "Bienvenido, Administrador", "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
            // Mostrar el MenuPrincipal para el administrador
            MenuPrincipal menu = new MenuPrincipal();
            menu.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Bienvenido, usuario " + nombreUsuario, "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
            
            
            
        }
        this.dispose(); // Cierra la ventana de inicio de sesión actual
    } else {
        JOptionPane.showMessageDialog(this, "Gmail o contraseña incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
    }




    }//GEN-LAST:event_btnIniciarSesionActionPerformed

