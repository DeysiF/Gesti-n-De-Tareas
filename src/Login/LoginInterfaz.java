    /**
     * Verifica las credenciales del administrador.
     * 
     * @param correo el correo electrónico ingresado.
     * @param contraseña la contraseña ingresada.
     * @return true si las credenciales coinciden con las del administrador; false en caso contrario.
     */
    private boolean verificarAdmin(String correo, String contraseña) {
        try (BufferedReader reader = new BufferedReader(new FileReader("admin.txt"))) {
            String linea = reader.readLine();
            if (linea != null) {
                String[] partes = linea.split(",");
                return partes[0].equals(correo) && partes[1].equals(contraseña);
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo de administrador", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }
    
    /**
     * Verifica las credenciales de un usuario normal.
     * 
     * @param correo el correo electrónico ingresado.
     * @param contraseña la contraseña ingresada.
     * @return true si las credenciales coinciden con las de un usuario registrado; false en caso contrario.
     */
    private boolean verificarUsuario(String correo, String contraseña) {
        try (BufferedReader reader = new BufferedReader(new FileReader("usuarios.txt"))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] partes = linea.split(",");
                if (partes[0].equals(correo) && partes[1].equals(contraseña)) {
                    return true;
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo de usuarios", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }
    

